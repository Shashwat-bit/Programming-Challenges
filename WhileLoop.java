
public class WhileLoop {
    public static void main(String[] args) {
        System.out.print("ye lo bhai 15 ka table ");

        int a =0;                      //initialization
        while (a <= 150) {             //Condition
            System.out.println(a);     //Actual body
            a=a+15;                    //Updating condition
        }
    }
}
